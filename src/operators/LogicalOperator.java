package operators;

public class LogicalOperator {

	public static void main(String[] args) {
		System.out.println(12&10);
		System.out.println(12|10);
		System.out.println(12^10);
		System.out.println(~10);
		System.out.println(~-10);
		System.out.println(-15>>3);
		System.out.println(15>>3);
		System.out.println(15<<3);
		System.out.println(-15<<3);
	
        }
}
